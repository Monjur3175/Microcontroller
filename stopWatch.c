// Common Anode (CA) 7-segment array 0-9
unsigned char array[] = {0xC0, 0xF9, 0xA4, 0xB0, 0x99, 0x92, 0x82, 0xF8, 0x80, 0x90};
int count = 0;
short running = 0; // ???????? ???? ?? ?? ?? ????? ???? (0 = Stop, 1 = Running)

void main() {
    TRISB = 0x00;   // PORTB ?????? (Segments)
    TRISD.F0 = 1;   // RD0 ????? (Start Button)
    TRISD.F1 = 1;   // RD1 ????? (Stop Button)

    // ???????? ??? ??? ???????? ???? ???? (????? ?????????? RD2 ?? RD3 ?? ????? ????)
    // ??? ?????? VCC ?? ???? ??? ????? ?????? ??????? ???
    TRISC.F0 = 0;
    PORTC.F0 = 1;   // Common Anode ??? ?? ???

    PORTB = array[count]; // ?????? '0' ??????

    while(1) {
        // --- START ???? ??? (RD0) ---
        if (PORTD.F0 == 1) {
            Delay_ms(20); // Debounce
            if (PORTD.F0 == 1) {
                running = 1; // ???????? ???? ???
            }
        }

        // --- STOP ???? ??? (RD1) ---
        if (PORTD.F1 == 1) {
            Delay_ms(20); // Debounce
            if (PORTD.F1 == 1) {
                running = 0; // ???????? ???? ???
            }
        }

        // --- ???????? ???? ---
        if (running == 1) {
            PORTB = array[count];
            Delay_ms(500); // ???????? ????? (?.? ??????? ?? ?? ?????)

            count++;
            if (count > 9) {
                count = 0; // ? ?? ?? ???? ? ???? ????
            }

            // ???????? ???????? 'Stop' ???? ????? ??? ???? ????
            if (PORTD.F1 == 1) running = 0;
        }
    }
}